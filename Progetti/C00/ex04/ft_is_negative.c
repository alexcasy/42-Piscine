#include <unistd.h>

void	ft_is_negative(int n)
{
	char	negy;
	char	posy;

	negy = 'N';
	posy = 'P';
	if (n < 0)
		write (1, &negy, 1);
	else
	{
		write (1, &posy, 1);
	}
}
