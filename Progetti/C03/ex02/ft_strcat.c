#include <unistd.h>

char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	t;

	i = 0;
	t = 0;
	while (dest[i])
	{
		i ++;
	}
	while (src[t])
	{
		dest[i] = src[t];
		i ++;
		t ++;
	}
	dest[i] = '\0';
	return (dest);
}
